<template>
    <div class="dropdown">
        <a class="dropdown-toggle" href="#" id="dropdownLocalePickerLink" data-bs-toggle="dropdown" aria-expanded="false">
            <i class="bi bi-globe2 me-2"></i>{{ $t('locale') }}
        </a>

        <ul class="dropdown-menu" aria-labelledby="dropdownLocalePickerLink">
            <li><a href="javascript:void(0);" @click="setLocale('uk')">Українська</a></li>
            <li><a href="javascript:void(0);" @click="setLocale('en')">English</a></li>
            <li><a href="javascript:void(0);" @click="setLocale('es')">Español</a></li>
        </ul>
    </div>
</template>

<script>
export default {
    name: "LocalePicker",
    methods: {
        setLocale(locale) {
            this.$store.commit('app/SET_LOCALE', locale);
            this.$root.$i18n.locale = locale;
        }
    }
}
</script>

